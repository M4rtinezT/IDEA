package day10;

public class Girl {
	private String name;
	private int age;
	
	public Girl() {
		
	}
	
	
	public Girl(String name, int age) {
		super();
		this.name = name;
		this.age = age;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
